USE HighSchoolStudentManagement_Group5;
GO

CREATE TABLE AcademicYear (
    AcademicYearID INT PRIMARY KEY,
    YearName VARCHAR(20),      -- '2024-2025'
    StartYear INT,
    EndYear INT
);

CREATE TABLE Teacher (
    TeacherID INT PRIMARY KEY,
    FullName VARCHAR(100),
    PhoneNumber VARCHAR(15)
);


CREATE TABLE Class (
    ClassID INT PRIMARY KEY,
    ClassName VARCHAR(50),
    Grade INT,                -- 10, 11, 12
    TeacherID INT,            -- GVCN
    AcademicYearID INT,
    FOREIGN KEY (TeacherID) REFERENCES Teacher(TeacherID),
    FOREIGN KEY (AcademicYearID) REFERENCES AcademicYear(AcademicYearID)
);

CREATE TABLE [Subject] (
    SubjectID INT PRIMARY KEY,
    SubjectName VARCHAR(100),
    TotalPeriods INT
);

CREATE TABLE Semester (
    SemesterID INT PRIMARY KEY,
    SemesterName VARCHAR(20),    -- Semester 1, Semester 2
    StartDate DATE,
    EndDate DATE,
    AcademicYearID INT,
    FOREIGN KEY (AcademicYearID) REFERENCES AcademicYear(AcademicYearID)
);

CREATE TABLE Student (
    StudentID INT PRIMARY KEY,
    Name VARCHAR(100),
    DateOfBirth DATE,
    Gender VARCHAR(10),
    Address VARCHAR(255),
    PhoneNumber VARCHAR(15),
    ClassID INT,
    FOREIGN KEY (ClassID) REFERENCES Class(ClassID)
);

CREATE TABLE Result (
    StudentID INT,
    SubjectID INT,
    SemesterID INT,
    --ClassID INT,
    OralScore DECIMAL(4,2),
    Score15Minutes DECIMAL(4,2),
    Score1Period DECIMAL(4,2),
    FinalExamScore DECIMAL(4,2),
    PRIMARY KEY (StudentID, SubjectID, SemesterID),
    FOREIGN KEY (StudentID) REFERENCES Student(StudentID),
    FOREIGN KEY (SubjectID) REFERENCES Subject(SubjectID),
    FOREIGN KEY (SemesterID) REFERENCES Semester(SemesterID),
    --FOREIGN KEY (ClassID) REFERENCES Class(ClassID)
);

CREATE TABLE Teaching (
    ClassID INT,
    TeacherID INT,
    SubjectID INT,
    AcademicYearID INT,
    PRIMARY KEY (ClassID, TeacherID, SubjectID, AcademicYearID),
    FOREIGN KEY (ClassID) REFERENCES Class(ClassID),
    FOREIGN KEY (TeacherID) REFERENCES Teacher(TeacherID),
    FOREIGN KEY (SubjectID) REFERENCES Subject(SubjectID),
    FOREIGN KEY (AcademicYearID) REFERENCES AcademicYear(AcademicYearID)
);

